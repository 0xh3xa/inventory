    private void updateInterfaceSelf() {
        //init obj.
        setSupplierList(new ArrayList<SupplierModel>());

        ArrayList<CityModel> cities = CityController.getAllCity();
        DefaultComboBoxModel cityModel = new DefaultComboBoxModel();
        getjComboBoxBanchCity().setModel(cityModel);
        for (CityModel city : cities) {
            cityModel.addElement(city);
        }

        getjTextFieldBranchSeach().getDocument().addDocumentListener(new DocumentListener() {

            @Override
            public void insertUpdate(DocumentEvent de) {
                aTextChanged();
            }

            @Override
            public void removeUpdate(DocumentEvent de) {
                aTextChanged();
            }

            @Override
            public void changedUpdate(DocumentEvent de) {
                aTextChanged();
            }

        });

        toggleBranchPanel(getjPanel2(), false);
    }

    private void aTextChanged() {
        String pattern = getjTextFieldBranchSeach().getText();
        if (pattern == "") {
            setBranchTableModel(new BranchTableModel(supplier.getBranchList()));
            getjTable1().setModel(getBranchTableModel());
            refershBranchTable();

        } else {
            BranchTableModel newBranchTableModel = new BranchTableModel(new ArrayList<BranchModel>());

            for (BranchModel branch : supplier.getBranchList()) {
                if (branch.contains(pattern)) {
                    newBranchTableModel.addRow(branch);

                }
            }
            getjTableBranch().setModel(newBranchTableModel);
            refershBranchTable();
        }
    }

