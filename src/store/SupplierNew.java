    private void aBranchAdd() {

        String branchName = getjTextFieldBranchName().getText();
        String branchAddress = getjTextFieldBranchAddress().getText();
        CityModel city = (CityModel) getjComboBoxBanchCity().getSelectedItem();
        BranchModel branch = new BranchModel(supplier, city, branchName, branchAddress);

        BranchController.setUpdateBranch(Operation.INSERT, branch);

        getBranchTableModel().addRow(branch);
        supplier.getBranchList().add(branch);

        refershBranchTable();
        refershBranchNewPanel();

    }

    private void aBranchDelete() {
        int[] rows = getjTableBranch().getSelectedRows();
        BranchModel branch = null;
        for (int i : rows) {
            branch = getBranchTableModel().getRow(i);
            BranchController.setUpdateBranch(Operation.DELETE, branch);
        }
        supplier.getBranchList().remove(branch);
        getBranchTableModel().removeRowFrom(rows.length);
        refershBranchTable();

    }

    private void aBranchEdit() {

        String text = getjButtonBranchEdit().getText();

        if (text.equals("edit")) {

            getjButtonBranchEdit().setText("update");

            int row = getjTableBranch().getSelectedRow();

            selectedBranch = getBranchTableModel().getRow(row);
            getjTextFieldBranchName().setText(selectedBranch.getName());
            getjTextFieldBranchAddress().setText(selectedBranch.getAddress());
            getjComboBoxBanchCity().setSelectedItem(selectedBranch.getCity());

            getBranchTableModel().removeRow(selectedBranch);
            supplier.getBranchList().remove(selectedBranch);

            refershBranchTable();
            toggleBranchButton(false);
            toggleSupplierPanel(false);

        } else if (text.equals("update")) {

            getjButtonBranchEdit().setText("edit");

            CityModel city = (CityModel) getjComboBoxBanchCity().getSelectedItem();
            String name = getjTextFieldBranchName().getText();
            String address = getjTextFieldBranchAddress().getText();
            selectedBranch.setName(name);
            selectedBranch.setAddress(address);
            selectedBranch.setCity(city);

            BranchController.setUpdateBranch(Operation.UPDATE, selectedBranch);

            //ArrayList<BranchModel> branches = BranchController.getAllBranch(supplier);
            branchTableModel = new BranchTableModel(supplier.getBranchList());
            supplier.getBranchList().add(selectedBranch);

            refershBranchTable();
            refershBranchNewPanel();

            toggleBranchButton(true);
            toggleSupplierPanel(true);

        }
    }

    public void toggleBranchPanel(JPanel containter, boolean status) {

        Component[] components = containter.getComponents();
        for (Component component : components) {
            try {
                JPanel panel = (JPanel) component;
                toggleBranchPanel((JPanel) component, status);

            } catch (Exception e) {
                component.setEnabled(status);
            }
        }

    }

