    private void aAdd() {

        try {
            String userName = jTextField_UserName.getText();
            String password = jTextField_Password.getText();

            MessageDigest md = MessageDigest.getInstance("MD5");

            byte[] enrypPass = md.digest(password.getBytes());
            password=new String(enrypPass);
            String phone = jTextField_Phone.getText();
            String email = jTextField_Email.getText();
            TypeUserModel typeUser = (TypeUserModel) jList1.getSelectedValue();

            user = new UserModel(userName, password, email, phone, typeUser);

            setClosed(true);
            dispose();
        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(UserNewDialog.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

