    private void updateInterFaceSelf(Frame parent) {
        setLocationRelativeTo(parent);
        getAllTypes();
    }

    private void getAllTypes() {
        DefaultListModel model = new DefaultListModel();
        jList1.setModel(model);
        ArrayList<TypeUserModel> types = TypeUserController.getTypes();
        for (TypeUserModel typeUserModel : types) {
            model.addElement(typeUserModel);
        }
    }

