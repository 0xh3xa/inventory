    private void updateInterfaceSelf() {
        DefaultComboBoxModel catModel = new DefaultComboBoxModel();
        jComboBoxCatogery.setModel(catModel);
        ArrayList<CatogeryModel> cats = CatogeryController.getAllCat();

        for (int i = 0; i < cats.size(); i++) {
            catModel.addElement(cats.get((i)));
        }

        DefaultComboBoxModel supModel = new DefaultComboBoxModel();
        jComboBoxSupplier.setModel(supModel);
        ArrayList<SupplierModel> allSupplier = SupplierController.getAllSupplier();
        for (SupplierModel supplierModel : allSupplier) {
            supModel.addElement(supplierModel);
        }
    }
