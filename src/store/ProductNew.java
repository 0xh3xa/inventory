    private void aAdd() {
        product.setName(jTextFieldName.getText());
        CatogeryModel cat = (CatogeryModel) jComboBoxCatogery.getSelectedItem();
        product.setCat(cat);
        System.out.println(cat.getId());
        double orgPrice = Double.parseDouble(jTextFieldPrice.getText());
        double disPrice = Double.parseDouble(jTextFieldDiscount.getText());
        product.setPrice(product.new Price(orgPrice, disPrice));
        product.setQuantity(Integer.parseInt(jTextFieldQuantity.getText()));
        product.setSupplier((SupplierModel) jComboBoxSupplier.getSelectedItem());
        closed = true;
        this.dispose();
    }

